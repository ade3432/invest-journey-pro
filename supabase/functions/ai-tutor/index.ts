import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const openAIApiKey = Deno.env.get('OPENAI_API_KEY');

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

const systemPrompt = `You are TradeUp AI, an expert trading tutor and mentor. Your role is to help users learn about:
- Stock market fundamentals (how markets work, order types, market hours)
- Cryptocurrency basics (blockchain, wallets, exchanges)
- Technical analysis (chart patterns, indicators like RSI, MACD, moving averages)
- Fundamental analysis (reading financial statements, valuations)
- Risk management (position sizing, stop losses, portfolio diversification)
- Trading psychology (managing emotions, discipline)
- Market terminology and concepts

Guidelines:
- Be encouraging and supportive - learning to trade takes time
- Use simple language and explain jargon when needed
- Give practical examples when possible
- If asked about specific trades or financial advice, remind users this is educational only
- Keep responses concise but informative
- Use emojis sparingly to keep things friendly ðŸ“ˆ
- If you don't know something, say so honestly`;

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    
    if (!openAIApiKey) {
      throw new Error('OPENAI_API_KEY is not configured');
    }

    const response = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${openAIApiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'gpt-4o-mini',
        messages: [
          { role: 'system', content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error('OpenAI API error:', response.status, errorText);
      throw new Error(`OpenAI API error: ${response.status}`);
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, 'Content-Type': 'text/event-stream' },
    });
  } catch (error) {
    console.error('Error in ai-tutor function:', error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : 'Unknown error' }), {
      status: 500,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  }
});
